<?php
// Pengulangan

// for
for ($i = 0; $i < 5; $i++) {
    echo "Hello World! (pengulangan for) <br>";
}

echo "<br>";

// while
$i = 0;
while ($i < 5) {
    echo "Hello World! (pengulangan while) <br>";
    $i++;
}

echo "<br>";

// do.. while
$i = 0;
do {
    echo "Hello World! (pengulangan do.. while) <br>";
    $i++;
} while ($i < 5);
